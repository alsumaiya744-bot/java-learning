class LargerNumber
{
	public static void main(String[] args)
	{
		byte a=15;
		byte b=38;
		if(a>b)
		{
			System.out.println(a+"is a largest number");
		}
		else
		{
			System.out.println(b+"is a largest number");
		}
		byte c=20;
		byte d=13;
		if(c>d)
		{
			System.out.println(c+"is a largest number");
		}
		else
		{
			System.out.println(d+"is a largest number");
		}
	}
	
}