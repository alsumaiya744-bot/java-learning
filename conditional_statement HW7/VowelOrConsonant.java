class VowelOrConsonant
{
	public static void main(String[] args)
	{
		char ch1='a';
		if((ch1=='a')||(ch1=='e')||(ch1=='i')||(ch1=='o')||(ch1=='u'))
		{
			System.out.println(ch1+"is a vowel");
		}
		else
		{
			System.out.println(ch1+"is a consonant");
		}
		char ch2='i';
		if((ch2=='a')||(ch2=='e')||(ch2=='i')||(ch2=='o')||(ch2=='u'))
		{
			System.out.println(ch2+"is a vowel");
		}
		else
		{
			System.out.println(ch2+"is a consonant");
		}
		char ch3='z';
		if((ch3=='a')||(ch3=='e')||(ch3=='i')||(ch3=='o')||(ch3=='u'))
		{
			System.out.println(ch3+"is a vowel");
		}
		else
		{
			System.out.println(ch3+"is a consonant");
		}
		
	}
}