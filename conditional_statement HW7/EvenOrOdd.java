class EvenOrOdd
{
	public static void main(String[] args)
	{
		int num1=26;
		int num2=35;
		if(num1%2==0)
		{
			System.out.println(num1+ "is even number");
		}
		else
		{
			System.out.println(num1+ "is odd number");
		}
		if(num2%2==0)
		{
			System.out.println(num2+ "is even number");
		}
		else
		{
			System.out.println(num2+ "is odd number");
		}
	}
	
}